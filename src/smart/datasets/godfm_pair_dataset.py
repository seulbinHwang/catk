"""Dataset of pre-computed GOD-FM (anchor_hidden_c_shift, tau_target) pairs.

Each pair was generated by running the Teacher model's goal-guided ODE
sampler on c_shift contexts captured during closed-loop rollout.
Stored on disk as chunked .pt files.
"""
from __future__ import annotations

from pathlib import Path

import torch
from torch import Tensor
from torch.utils.data import Dataset


class GodFMPairDataset(Dataset):
    """Loads pre-generated (anchor_hidden, tau_target) pairs from disk.

    Expects a directory of .pt files each containing a dict with keys:
        "anchor_hidden": [N, hidden_dim]  float tensor
        "tau_target":    [N, 20, 4]       float tensor

    Args:
        pair_dir: Directory containing pair_*.pt chunk files.
    """

    def __init__(self, pair_dir: str) -> None:
        super().__init__()
        chunk_files = sorted(Path(pair_dir).glob("pairs_*.pt"))
        if not chunk_files:
            raise FileNotFoundError(
                f"No pairs_*.pt files found in {pair_dir}"
            )

        hiddens: list[Tensor] = []
        targets: list[Tensor] = []
        for f in chunk_files:
            chunk = torch.load(f, map_location="cpu", weights_only=True)
            hiddens.append(chunk["anchor_hidden"])
            targets.append(chunk["tau_target"])

        self.anchor_hidden: Tensor = torch.cat(hiddens, dim=0)  # [N, hidden_dim]
        self.tau_target: Tensor = torch.cat(targets, dim=0)     # [N, 20, 4]

    def __len__(self) -> int:
        return int(self.anchor_hidden.shape[0])

    def __getitem__(self, idx: int) -> dict[str, Tensor]:
        return {
            "anchor_hidden": self.anchor_hidden[idx],
            "tau_target": self.tau_target[idx],
        }
